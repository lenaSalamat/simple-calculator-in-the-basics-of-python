#return the sum of num1 and num2
def add(num1 ,num2):
	return num1 + num2

#return the result of subtracting num1 - num2
def sub(num1 ,num2):
	return num1 - num2

#return the result of multiplying num1 - num2
def mul(num1 ,num2):
	return num1 * num2

#return the result of dividing num1 - num2
def div(num1 ,num2):
	return num1 / num2	

